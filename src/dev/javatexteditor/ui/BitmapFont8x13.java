    /**
     * codePoint のグリフを指定セルサイズで色付きの BufferedImage にレンダリングして返す。
     * 点灯ピクセルを fgRgb（不透明）、非点灯ピクセルを透明（alpha=0）とする。
     */
    public static BufferedImage renderGlyph(int codePoint, int cellW, int cellH, int fgRgb) {
        return FixedFontRenderer.renderGlyph(GLYPHS, BASE_CELL_W, BASE_CELL_H, BYTES_PER_ROW,
            FIRST_CHAR, LAST_CHAR, codePoint, cellW, cellH, fgRgb);
    }

    /**
     * ASCII 範囲内かどうかを返す。
     */
    public static boolean isSupported(int codePoint) {
        return codePoint >= FIRST_CHAR && codePoint <= LAST_CHAR;
    }

